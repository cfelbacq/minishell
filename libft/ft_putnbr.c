#include "libft.h"

void	ft_putnbr(int n)
{
	int i;

	i = 1;
	if (n == -2147483648)
	{
		ft_putstr("-2147483648");
		return ;
	}
	if (n < 0)
	{
		ft_putchar('-');
		n = n * -1;
	}
	while ((n / i) >= 10)
	{
		i = i * 10;
	}
	while (i > 0)
	{
		ft_putchar((n / i) % 10 + '0');
		i = i / 10;
	}
}
