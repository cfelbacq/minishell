#include "libft.h"

char	*ft_strncat(char *dest, const char *src, size_t n)
{
	size_t size_dest;
	size_t i;

	if (n == 0)
		return (dest);
	size_dest = ft_strlen(dest);
	i = 0;
	while (i < n && src[i] != '\0')
	{
		dest[size_dest + i] = src[i];
		i++;
	}
	dest[size_dest + i] = '\0';
	return (dest);
}
